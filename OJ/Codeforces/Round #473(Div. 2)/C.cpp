#include<iostream>
#include<cstdio>
#include<vector>

using namespace std;

int n;

void solve(){
    if(n <= 5) cout << "-1\n";
    else{
        for(int i = 2; i <= n; i += 2){
            printf("1 %d\n", i);
        }
        for(int i = 3; i <= n; i += 2){
            printf("%d %d\n", i, n - n % 2);
        }
    }
    for(int i = 1; i <= n - 1; i++){
        printf("%d %d\n", i, i + 1);
    }
}
int main(){
    cin >> n;
    solve();
    return 0;
}