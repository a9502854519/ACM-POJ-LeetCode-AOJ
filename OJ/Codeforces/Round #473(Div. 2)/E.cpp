#include<iostream>
#include<cstdio>

using namespace std;
typedef long long ll;

ll n;

void solve(){
    ll res = 0;
    n--;
    for(ll i = 1;; i <<= 1){
        if((n + i) / (i << 1) == 0) break;
        res += ((n + i) / (i << 1)) * i;
    }
    cout << res << endl;
}
int main(){
    cin >> n;
    solve();
    return 0;
}