#include<iostream>
#include<cstdio>
#include<vector>
#include<cstring>
#include<algorithm>

using namespace std;

int n, s;

int main(){
    int res = 0;
    cin >> n >> s;
    for(int i = 0, h, m; i < n; i++){
        scanf("%d %d\n", &h, &m);
        m += h * 60;
        if(m - res <= s) res = m + s + 1;
        else break;
    }
    printf("%d %d\n", res / 60, res % 60);
    return 0;
}